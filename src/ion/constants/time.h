/*
  Defines some constant numbers to deal with times.
  The time baseline is set to milliseconds.
*/
#define MILLISECOND   1
#define MILLISECONDS  1
#define SECOND        1000
#define SECONDS       1000
#define MINUTE        60*SECOND
#define MINUTES       60*SECONDS
#define HOUR          60*MINUTE
#define HOURS         60*MINUTES
#define DAY           24*HOUR
#define DAYS          24*HOURS
#define WEEK          7*DAY
#define WEEKS         7*DAYS
