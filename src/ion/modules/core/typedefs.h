/**
 * Defines all standardized ION numerical types.  */

typedef uint8_t         u8;       /* <stdint.h> */
typedef uint16_t        u16;      /* <stdint.h> */
typedef uint32_t        u32;      /* <stdint.h> */
typedef uint64_t        u64;      /* <stdint.h> */
typedef int8_t          i8;       /* <stdint.h> */
typedef int16_t         i16;      /* <stdint.h> */
typedef int32_t         i32;      /* <stdint.h> */
typedef int64_t         i64;      /* <stdint.h> */
typedef float           d32;
typedef double          d64;
typedef long double     d128;
typedef unsigned char   byte;
typedef size_t          addr;     /* <stddef.h> */
